package exercise01;

public class Main {

	public static void main(String[] args) {

		//北ソフト工房練習問題

		//練習1-1
		System.out.println("Hello World");

		//練習1-2
		int x2 = 11;
		System.out.println("x = " + x2);

		//練習1-3
		int x3 = 13;
		int y3 = 17;
		System.out.println("x = " + x3 + ", y = " + y3);

		//練習1-4
		int x4 = 13 + 17;
		System.out.println(x4);

		//練習1-5
		System.out.println(13 * 17);

		//練習1-6
		int x6 = 7;
		x6 *= 3;
		System.out.println(x6);
		x6 /= 2;
		System.out.println(x6);

		//練習1-7
		int x7 = 3;
		int y7 = 7;

		int z7 = x7;
		x7 = y7;
		y7 = z7;
		System.out.println( "x = " + x7 + ",y = " + y7 );

		//練習1-8
		int x8 = 19;
		int y8 = 23;
		int z8 = x8 * y8;
		System.out.println("z = " + z8);

		//練習1-9
		int x9 = 20;
		System.out.println(x9 * 2);
		System.out.println(x9 * 3);
		System.out.println(x9 * 4);

		//練習1-10
		int x10 = 10;
		System.out.println(x10);
		System.out.println(x10 * x10);
		System.out.println(x10 * x10 * x10);

		//練習1-11


	}

}
